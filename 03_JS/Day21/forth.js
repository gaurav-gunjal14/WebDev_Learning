//oprators
// &&  -> and
// ||  -> or
// !   -> not
// &   -> bitwise and op. compare two no. in binary by and gate and return no.
// |   -> bitwise or
// ^   -> bitwise XOR
// <<  -> first no. * 2^ second no.
// >>  -> first no. / 2^ second no.
let age = 19;
let money = 1000;

console.log(age>18 && money>500)
console.log(age>18 || money>500)
console.log(!(age>18))
console.log(12&10)
console.log(5<<3)
console.log(20>>3)